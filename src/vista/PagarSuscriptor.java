    private void btnPagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPagarActionPerformed
        // TODO add your handling code here:
        try {
            int code = Integer.valueOf(txtBuscar.getText());
            int valor = Integer.valueOf(txtCantidadPagar.getText());
            boolean reconexion = cbReconexion.isSelected();
            boolean matricula = cbMatricula.isSelected();
            if (controller.cobrarDeuda(code, valor, reconexion, matricula)) {
                JOptionPane.showMessageDialog(rootPane, "Pago exitoso realizado al suscriptor","Pagar",JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(rootPane, "Pago fallido al suscriptor","Pagar",JOptionPane.INFORMATION_MESSAGE);
            }
            clean();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Por favor realize la busqueda de un suscriptor","Pagar",JOptionPane.INFORMATION_MESSAGE);
            e.getMessage();
        }
    }//GEN-LAST:event_btnPagarActionPerformed

    public void clean(){
        txtBuscar.setText(null);
        txtSuscriptor.setText(null);
        txtDeudaTotal.setText(null);
        txtCantidadPagar.setText(null);
    }
    
