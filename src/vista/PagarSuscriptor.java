    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        try {
            int code = Integer.valueOf(txtBuscar.getText());
            Acueducto suscriptor = controller.obtenerSuscriptor(code);
            if (suscriptor != null) {
                JOptionPane.showMessageDialog(rootPane, "Busqueda exitosa del suscriptor",
                        "Busqueda", JOptionPane.INFORMATION_MESSAGE);
                txtSuscriptor.setText(suscriptor.getNombres() + " " + suscriptor.getApellidos());
                txtDeudaTotal.setText(String.valueOf(suscriptor.getTotalPagar()));
            } else {
                JOptionPane.showMessageDialog(rootPane, "Busqueda fallida del suscriptor. "
                        + "\n El codigo ingresado no esta regitrado.",
                        "Busqueda", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Por favor, ingrese un codigo",
                    "Busqueda", JOptionPane.INFORMATION_MESSAGE);
            e.getMessage();
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void txtBuscarKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscarKeyTyped
        // TODO add your handling code here:
        char s = evt.getKeyChar();
        if ((s < '0' || s > '9') && (s != KeyEvent.VK_BACKSPACE)) {
            evt.consume();
        }
    }//GEN-LAST:event_txtBuscarKeyTyped

