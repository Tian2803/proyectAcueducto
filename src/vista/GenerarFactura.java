    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        try {
            int code = Integer.valueOf(txtBuscar.getText());
            modelo.Acueducto suscriptor = controller.obtenerSuscriptor(code);
            if (suscriptor != null) {
                JOptionPane.showMessageDialog(rootPane, "Busqueda exitosa del suscriptor.",
                        "Busqueda", JOptionPane.INFORMATION_MESSAGE);
                txtNombre.setText(suscriptor.getNombres() + " " + suscriptor.getApellidos());
                txtDireccion.setText(suscriptor.getDireccion());
                txtCodigo.setText(String.valueOf(suscriptor.getCodigo()));
                txtFechaDeEmision.setText(suscriptor.getfEmision());
                txtPagoOportuno.setText(suscriptor.getPagoOportuno());
                txtFechaSuspension.setText(suscriptor.getfSuspension());
                txtMesFacturado.setText(suscriptor.getMesFacturado());
                txtMesesVencidos.setText(String.valueOf(suscriptor.getMesesVencidos()));
                txtAlcantarillado.setText(suscriptor.getAlcantarillado());
                txtAseo.setText(suscriptor.getAseo());
                txtTarifa.setText(suscriptor.getTarifa());
                txtSubsidio.setText(suscriptor.getSubsidio());
                txtConsumo.setText(suscriptor.getConsumo());
                txtTotalPresente.setText("$" + String.valueOf(suscriptor.getTotalPresente()));
                txtMesesDeuda.setText(String.valueOf(suscriptor.getMesesDeuda()));
                txtMatricula.setText(String.valueOf(suscriptor.getMatricula()));
                txtReconexion.setText(String.valueOf(suscriptor.getReconexion()));
                txtTotalOtrosSaldos.setText("$" + String.valueOf(suscriptor.getReconexion() + suscriptor.getMatricula()));
                txtTotalPagar.setText("$" + String.valueOf(suscriptor.getTotalPagar()));
                txtFacturaNumero.setText(txtFacturaNumero.getText() + String.valueOf(suscriptor.getCodigo()));
                txtNombreSuscriptor.setText(suscriptor.getNombres() + " " + suscriptor.getApellidos());
                txtCodigoSuscriptor.setText(String.valueOf(suscriptor.getCodigo()));
                txtFEmisionSuscriptor.setText(suscriptor.getfEmision());
                txtPOportunoSuscriptor.setText(suscriptor.getPagoOportuno());
                txtFSuspensionSuscriptor.setText(suscriptor.getfSuspension());
                txtMesFacturadoSuscriptor.setText(suscriptor.getMesFacturado());
                txtConsumoSuscriptor.setText(suscriptor.getConsumo());
                txtTPresenteSuscriptor.setText("$" + String.valueOf(suscriptor.getTotalPresente()));
                txtMDeudaSuscriptor.setText(String.valueOf(suscriptor.getMesesDeuda()));
                txtMatriculaSuscriptor.setText(String.valueOf(suscriptor.getMatricula()));
                txtReconexionSuscriptor.setText(String.valueOf(suscriptor.getReconexion()));
                txtOSaldosSuscriptor.setText("$" + String.valueOf(suscriptor.getReconexion() + suscriptor.getMatricula()));
                txtTotalAcueducto.setText("$" + String.valueOf(suscriptor.getTotalPagar()));
            } else {
                JOptionPane.showMessageDialog(rootPane, "Busqueda fallida no se encontro el suscriptor.",
                        "Busqueda", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "ERROR...Digite un codigo...", "Busqueda", JOptionPane.ERROR_MESSAGE);
            e.getMessage();
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImprimirActionPerformed
        // TODO add your handling code here:
        try {
            Toolkit tk = pnlFactura.getToolkit();
            PrintJob pj = tk.getPrintJob(this, null, null);
            Graphics g = pj.getGraphics();
            pnlFactura.print(g);
            g.dispose();
            pj.end();
            JOptionPane.showMessageDialog(rootPane, "PDF generado exitosamente","PDF",JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception e) {
            e.getMessage();
            JOptionPane.showMessageDialog(rootPane, "No se logro generar el PDF","PDF",JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnImprimirActionPerformed

